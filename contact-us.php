    <script>
        var siteKey = "<?php echo $site_key; ?>";
        window.onload = function() {
            $('button.btn-submit').click(function(event) {
                $('form.form-contact').find('.error').each(function() {
                    $(this).text('');
                })
                let valid = true;
                $('form.form-contact').find('input,textarea').each(function() {
                    if (!this.checkValidity()) {
                        $(this).siblings('.error').text(this.validationMessage) && (valid = false);
                    }
                });
                if ($('input[name="services[]"]:checked').length == 0) {
                    $('.check-list.services').siblings('.error').text(
                        'Select at least one service, you are intersted in.') && (valid = false);
                }
                if (!valid) {
                    return;
                }
                var services = [];
                $.each($('input[name="services[]"]:checked'), function() {
                    services.push($(this).val());
                });

                grecaptcha.ready(function() {
                    // do request for recaptcha token
                    // response is promise with passed token
                    grecaptcha.execute(siteKey, {
                            action: 'contact'
                        })
                        .then(function(token) {
                            // add token value to form
                            document.getElementById('g-recaptcha-response').value = token;
                            jQuery.ajax({
                                url: "contactus-handler.php",
                                data: 'name=' + $("#name").val() + '&email=' + $("#email")
                                    .val() + '&phone=' + $("#phone").val() + '&comments=' +
                                    $("#comments").val() + '&services=' + services +
                                    '&websiteurl=' + $("#url").val() +
                                    '&recaptcha_response=' + $("#g-recaptcha-response")
                                    .val(),
                                type: "POST",
                                beforeSend: function() {
                                    $('#spinner-border').show();
                                },
                                complete: function() {
                                    $('#spinner-border').hide();
                                },
                                success: function(result) {
                                    var data = jQuery.parseJSON(result);
                                    if (data.status) {
                                        $("form.form-contact")[0].reset();
                                        window.location.replace("thank-you.html");
                                        /* $(".alert-success").html(data.message).slideDown("slow");
                                        $(".alert-success").show(); */
                                    } else {
                                        $(".alert-danger").html(data.message).slideDown(
                                            "slow");
                                        $(".alert-danger").show();
                                    }
                                },
                                error: function() {}
                            });

                        });
                });

            });
        };
    </script>
